function Ressources(insanity = 0, rooms = 0, relics = 0, rituals = 0,
                    insanityMultiplier =1, roomsMultiplier =1, relicsMultiplier =1, ritualMulitplier =1) {
    this.insanity = insanity;
    this.rooms = rooms;
    this.relics = relics;
    this.rituals = rituals;

    this.insanityMultiplier = insanityMultiplier;
    this.roomsMultiplier = roomsMultiplier;
    this.relicsMultiplier = relicsMultiplier;
    this.ritualsMultiplier = ritualMulitplier;
}